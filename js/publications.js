/* ================================
   Safe Value Helper
================================ */
function safeValue(value) {
  return value && value.toString().trim() !== "" ? value : "-";
}

/* ================================
   Generate Year Tabs
================================ */
function generateYearTabs() {
  const tabsContainer = document.getElementById("yearTabs");
  tabsContainer.innerHTML = "";

  const years = Object.keys(publicationsData).sort(
    (a, b) => parseInt(b.split("-")[0]) - parseInt(a.split("-")[0])
  );

  years.forEach((year, index) => {
    const li = document.createElement("li");
    li.className = "nav-item";

    li.innerHTML = `
      <button class="nav-link ${index === 0 ? "active" : ""}"
        data-bs-toggle="tab"
        data-year="${year}"
        type="button">
        ${year}
      </button>
    `;

    tabsContainer.appendChild(li);
  });
}

/* ================================
   Load Publications into Table
================================ */
function loadPublications(year) {
  const tableBody = document.getElementById("publicationsTableBody");
  const countBox = document.getElementById("pubCount");

  const publications = publicationsData[year] || [];

  countBox.textContent = `Total Papers Published: ${publications.length}`;
  tableBody.innerHTML = "";

  publications.forEach((pub, index) => {
    const row = document.createElement("tr");

    row.innerHTML = `
      <td>${index + 1}</td>
      <td>${safeValue(pub.authors)}</td>
      <td>${safeValue(pub.department)}</td>
      <td>${safeValue(pub.title)}</td>
      <td>${safeValue(pub.journal)}</td>
      <td>${safeValue(pub.publication_date)}</td>
      <td>${safeValue(pub.issn)}</td>
      <td>
        ${
          pub.ugc_link && pub.ugc_link.trim() !== ""
            ? `<a href="${pub.ugc_link}" target="_blank">${pub.ugc_link}</a>`
            : "-"
        }
      </td>
      <td>${safeValue(pub.pages)}</td>
    `;

    tableBody.appendChild(row);
  });

  if (publications.length === 0) {
    tableBody.innerHTML = `
      <tr>
        <td colspan="9" class="text-center">-</td>
      </tr>
    `;
  }
}

/* ================================
   Initial Load & Tab Click
================================ */
document.addEventListener("DOMContentLoaded", () => {
  generateYearTabs();

  const years = Object.keys(publicationsData).sort(
    (a, b) => parseInt(b.split("-")[0]) - parseInt(a.split("-")[0])
  );

  const defaultYear = years[0];
  loadPublications(defaultYear);

  document.getElementById("yearTabs").addEventListener("click", (e) => {
    if (e.target.classList.contains("nav-link")) {
      document
        .querySelectorAll(".nav-link")
        .forEach(btn => btn.classList.remove("active"));

      e.target.classList.add("active");
      loadPublications(e.target.getAttribute("data-year"));
    }
  });
});
