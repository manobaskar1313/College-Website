    // Handle tab switching
    tabs.forEach((tab) => {
      tab.addEventListener("click", () => {
        tabs.forEach((t) => t.classList.remove("active"));
        tab.classList.add("active");
        showPhotos(tab.dataset.year);
      });
    });

    // Handle clicking on description hyperlink
    photoContainer.addEventListener("click", (e) => {
      if (e.target.tagName === "A") {
        e.preventDefault();
        const year = e.target.dataset.year;
        const index = e.target.dataset.index;
        const event = galleryData[year][index];

        // Show sub-gallery
        gallery.style.display = "none";
        subGallery.style.display = "block";

        subGalleryTitle.textContent = event.desc;
        subGalleryPhotos.innerHTML = event.subPhotos
          .map(
            (src) =>
              `<div class="photo-card"><img src="${src}" alt="${event.desc}"></div>`
          )
          .join("");
      }
    });

